public class Potion {
    String potionName;
    int potionValue;

    public Potion(String potionName, int potionValue) {
        this.potionName = potionName;
        this.potionValue = potionValue;
    }

    public String getPotionName() {
        return potionName;
    }

    public int getPotionValue() {
        return potionValue;
    }
    


}
